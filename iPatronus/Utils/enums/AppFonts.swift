import SwiftUICore

enum AppFonts {
  
  static func regularHPOne(_ size: CGFloat) -> Font {
    Font.custom("Harry P", size: size)
  }
  
  static func regularHPTwo(_ size: CGFloat) -> Font {
    Font.custom("Parry Hotter", size: size)
  }
}
